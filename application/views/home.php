                        <h3> <b> Tasikmalaya </b> </h3>
                        <p align="justify"> <b>Tasikmalaya</b> merupakan salah satu daerah di Jawa Barat yang memiliki berbagai destinasi pariwisata yang indah.
                        Selain dikenal dengan Kota Santri, Tasikmalaya juga menyimpan segudang tempat wisata yang menarik dan memukau, sehingga pas dikunjungi saat libur panjang.
                        Sejarah berdirinya Kota Tasikmalaya sebagai daerah otonomi tidak terlepas dari sejarah berdirinya kabupaten Tasikmalaya sebagai daerah kabupaten induknya. Maka rangkaian sejarah ini merupakan bagian dari rangakaian perjalanan Pemerintah Kabupaten Tasikmalaya sampai terbentuknya Pemerintah Kota Tasikmalaya. </p>
                        

                        <h3> <b> Sejarah Tasikmalaya </b> </h3>
                        <p align="justify"> Pada waktu A. Bunyamin menjabat sebagai Bupati Tasikmalaya tahun 1976 sampai dengan 1981 tonggak sejarah lahirnya kota Tasikmalaya dimulai denngan diresmikannya Kota Administratif Tasikmalaya melalui peraturan Pemerintah Nomor 22 Tahun 1976 oleh Menteri Dalam Negeri H. Amir Machmud. 
                            Peristiwa ini di tandai dengan penandatangan Prasasti yang sekarang terletak di depan gedung DPRD Kabupaten Tasikmalaya. Pada waktu yang sama dilantik pula Walikota Administratif Pertama yaitu Drs. H. Oman Roosman oleh Gubernur KDH Tingkat I Jawa Barat H. Aang Kunaefi.
                            Pada awal pembentukannya, wilayah kota Administratif Tasikmalaya meliputi 3 Kecamatan yaitu Cipedes, Cihideung dan Tawang dengan jumlah desa sebanyak 13 desa.</p>
                        
